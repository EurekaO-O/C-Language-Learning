/*---------------- 求一个整数存储在内存中的二进制中1的个数 ----------------*/
#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
void main() {
	int num = 0;
	printf("请输入：");
	scanf("%d",&num);
	int count = 0;
	for (int i = 0; i < 32; i++) {
		if (1 & (num >> i)) {
			count++;
		}
	}
	printf("%d出现了%d次\n",num,count);
}